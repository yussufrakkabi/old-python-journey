# variables_and_types.py

# Different basic data types
age = 27               # integer (int)
height = 1.70          # floating point number (float)
name = "Youssef"         # string (str)
is_student = True      # boolean (bool)
favorite_colors = ["blue", "black", "green"]   # list

print("Name:", name)
print("Age:", age)
print("Height:", height, "m")
print("Student?", is_student)
print("Favorite colors:", favorite_colors)

# Type checking
print("\nTypes:")
print(type(age))        # <class 'int'>
print(type(height))     # <class 'float'>
print(type(name))       # <class 'str'>
print(type(is_student)) # <class 'bool'>
print(type(favorite_colors))  # <class 'list'>